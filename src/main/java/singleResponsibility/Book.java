package singleResponsibility;

public class Book {

    public String name, authorName, isbn;
    public int year, price;

    public Book(String name, String authorName, String isbn, int year, int price)
    {
        this.name = name;
        this.authorName = authorName;
        this.isbn = isbn;
        this.year = year;
        this.price = price;

    }


}
